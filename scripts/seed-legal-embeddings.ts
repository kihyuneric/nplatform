async function seedEmbeddings() {
  console.log(`\n🌱 NPL 법률 문서 임베딩 시드 시작`)
  console.log(`📍 Target: ${BASE_URL}/api/v1/rag/ingest`)
  console.log(`📄 Documents: ${LEGAL_DOCUMENTS.length}개\n`)

  const response = await fetch(`${BASE_URL}/api/v1/rag/ingest`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'x-admin-secret': ADMIN_SECRET,
    },
    body: JSON.stringify({
      documents: LEGAL_DOCUMENTS,
      chunk_size: 600,
      chunk_overlap: 120,
    }),
  })

  if (!response.ok) {
    const text = await response.text()
    console.error(`❌ API 오류 ${response.status}: ${text}`)
    process.exit(1)
  }

  const result = await response.json()
  console.log('✅ 시드 결과:')
  console.log(JSON.stringify(result, null, 2))

  if (result.stats?.errors?.length > 0) {
    console.warn('\n⚠️  일부 오류 발생:')
    result.stats.errors.forEach((e: string) => console.warn(' -', e))
  }

  // 통계 조회
  const statsResponse = await fetch(`${BASE_URL}/api/v1/rag/ingest`, {
    headers: { 'x-admin-secret': ADMIN_SECRET },
  })
  if (statsResponse.ok) {
    const stats = await statsResponse.json()
    console.log('\n📊 현재 임베딩 DB 현황:')
    console.log(`총 청크: ${stats.total_chunks}개`)
    if (stats.by_source) {
      console.table(stats.by_source)
    }
  }
}

seedEmbeddings().catch(console.error)
