import type { CourtAuctionListing, AiVerdict, CreditorType, PropertyType } from './types'

function uid(n: number): string {
  return `mock-${String(n).padStart(4, '0')}-0000-0000-0000-000000000000`
}

function addDays(base: string, days: number): string {
  const d = new Date(base)
  d.setDate(d.getDate() + days)
  return d.toISOString().split('T')[0]!
}

const TODAY = '2026-04-05'

function bidDate(daysFromNow: number): string {
  return addDays(TODAY, daysFromNow)
}

type Row = Omit<CourtAuctionListing, 'id' | 'created_at' | 'updated_at'>

function makeRow(
  n: number,
  caseNum: string,
  courtName: string,
  propertyType: PropertyType,
  address: string,
  sido: string,
  sigungu: string,
  dong: string,
  areM2: number,
  floor: number,
  buildYear: number,
  appraised: number,
  auctionCount: number,
  creditorName: string,
  creditorType: CreditorType,
  loanBalance: number,
  tenantCount: number,
  tenantDeposit: number,
  hasOpposing: boolean,
  aiVerdict: AiVerdict,
  daysUntilBid: number,
  isFeatured: boolean
): CourtAuctionListing {
  const bidRate = 1 - (auctionCount - 1) * 0.1  // 1회=100%, 2회=80%, 3회=70% 등
  const minBid = Math.round(appraised * Math.max(bidRate, 0.6))
  const aiRoi = aiVerdict === 'STRONG_BUY' ? 18 + (n % 8)
    : aiVerdict === 'BUY' ? 10 + (n % 6)
    : aiVerdict === 'CONSIDER' ? 4 + (n % 5)
    : aiVerdict === 'CAUTION' ? -1 + (n % 3)
    : -8 + (n % 4)
  const aiRisk = aiVerdict === 'STRONG_BUY' ? 15 + (n % 10)
    : aiVerdict === 'BUY' ? 25 + (n % 15)
    : aiVerdict === 'CONSIDER' ? 40 + (n % 20)
    : aiVerdict === 'CAUTION' ? 60 + (n % 20)
    : 80 + (n % 15)

  return {
    id: uid(n),
    case_number: caseNum,
    court_name: courtName,
    court_code: null,
    property_type: propertyType,
    property_sub_type: null,
    address,
    sido,
    sigungu,
    dong,
    detail_address: null,
    latitude: null,
    longitude: null,
    area_m2: areM2,
    land_area_m2: null,
    floor,
    total_floors: 20 + (n % 10),
    build_year: buildYear,
    appraised_value: appraised,
    min_bid_price: minBid,
    winning_bid: null,
    winning_bid_rate: null,
    deposit_amount: Math.round(minBid * 0.1),
    status: 'SCHEDULED',
    auction_date: bidDate(daysUntilBid),
    auction_count: auctionCount,
    previous_min_bid: auctionCount > 1 ? Math.round(appraised * (bidRate + 0.1)) : null,
    result_at: null,
    creditor_name: creditorName,
    creditor_type: creditorType,
    loan_principal: Math.round(loanBalance * 0.85),
    loan_balance: loanBalance,
    total_claim: Math.round(loanBalance * 1.12),
    senior_claim: Math.round(loanBalance * 0.3),
    junior_claim: Math.round(loanBalance * 0.2),
    lien_count: 1 + (n % 3),
    seizure_count: n % 3,
    tenant_count: tenantCount,
    total_tenant_deposit: tenantDeposit,
    has_opposing_force: hasOpposing,
    lease_detail: tenantCount > 0 ? [
      { name: '임차인', deposit: tenantDeposit, from: '2023-01-01', to: '2025-01-01', opposing: hasOpposing }
    ] : [],
    ai_roi_estimate: Math.round(aiRoi * 100) / 100,
    ai_risk_score: Math.min(100, aiRisk),
    ai_bid_prob: aiVerdict === 'STRONG_BUY' ? 0.85 : aiVerdict === 'BUY' ? 0.72 : aiVerdict === 'CONSIDER' ? 0.55 : aiVerdict === 'CAUTION' ? 0.35 : 0.15,
    ai_verdict: aiVerdict,
    ai_reasoning: null,
    ai_screened_at: TODAY + 'T00:00:00Z',
    ai_model_version: 'v1',
    related_listing_id: null,
    external_id: null,
    source: 'COURT',
    raw_data: {},
    images: [],
    documents: [],
    notes: null,
    is_featured: isFeatured,
    view_count: 10 + (n * 7) % 300,
    bookmark_count: n % 20,
    created_at: addDays(TODAY, -30 + (n % 30)) + 'T00:00:00Z',
    updated_at: TODAY + 'T00:00:00Z',
  }
}

