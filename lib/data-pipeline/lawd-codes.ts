// ─── 공개 API ────────────────────────────────────────────────

/**
 * 시/도 + 시군구 이름으로 LAWD 5자리 코드 반환.
 * 매칭 실패 시 시도 prefix + '000' 반환 (예: '서울' + 알 수 없는 구 → '11000').
 */
export function getLawdCode(region: string, district: string = ''): string {
  const cleanRegion = region.replace(/특별시|광역시|도$/, '').trim()
  const cleanDistrict = district.replace(/\s+/g, '').trim()

  // 1) 정확 매칭
  const key = `${cleanRegion}|${cleanDistrict}`
  if (LAWD_TABLE[key]) return LAWD_TABLE[key]

  // 2) 구 이름만 (서울 '강남구', 경기 '수원시영통구' 등)
  if (cleanDistrict) {
    for (const [k, v] of Object.entries(LAWD_TABLE)) {
      const [kSido, kDistrict] = k.split('|')
      if (kSido === cleanRegion && kDistrict.startsWith(cleanDistrict)) return v
    }
  }

  // 3) prefix + '000'
  const prefix = SIDO_PREFIX[cleanRegion]
  return prefix ? `${prefix}000` : '00000'
}

/**
 * LAWD 5자리 → { region, district } 역조회.
 */
export function parseLawdCode(code: string): { region: string; district: string } | null {
  for (const [key, v] of Object.entries(LAWD_TABLE)) {
    if (v === code) {
      const [region, district] = key.split('|')
      return { region, district }
    }
  }
  const prefix = code.slice(0, 2)
  const region = Object.entries(SIDO_PREFIX).find(([, p]) => p === prefix)?.[0]
  return region ? { region, district: '' } : null
}

/**
 * 자유 주소 문자열에서 LAWD 코드 추출 (best-effort).
 * "서울특별시 강남구 역삼동 123-4" → "11680"
 */
export function extractLawdFromAddress(address: string): string {
  const addr = address.replace(/\s+/g, ' ').trim()
  // 시도 추출
  const sido = Object.keys(SIDO_PREFIX).find((s) => addr.includes(s)) ?? ''
  if (!sido) return '00000'

  // 구/군/시 추출
  const districtMatch = addr.match(/([가-힣]+(?:구|군|시))(?:\s|$)/g)
  if (districtMatch) {
    for (const d of districtMatch) {
      const code = getLawdCode(sido, d.trim())
      if (code !== '00000' && !code.endsWith('000')) return code
    }
  }
  return getLawdCode(sido)
}

/**
 * 전체 LAWD 코드 수.
 */
export function getLawdCodeCount(): number {
  return Object.keys(LAWD_TABLE).length
}
